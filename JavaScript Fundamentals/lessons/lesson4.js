// Relational or comparison operators

var result = 5 > 3;
console.log(result); // Output: true

var moreThan = 10 >= 10;
console.log(moreThan); // Output: true

var lessThan = 5 < 10;
console.log(lessThan); // Output: true

var lessThanOrEqual = 5 <= 5;
console.log(lessThanOrEqual); // Output: true

// Equality operators
var isEqual = 5 == 5;
console.log(isEqual); // Output: true (loose equality checks value only)
var isNotEqual = 5 != 3;
console.log(isNotEqual); // Output: true
var strictEqual = 5 === 5;
console.log(strictEqual); // Output: true (strict equality checks both value and type)
var strictNotEqual = 5 !== 3;
console.log(strictNotEqual); // Output: true
